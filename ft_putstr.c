#include "libft/libft.h"

int	ft_putstr(char *s)
{
	int	a;

	if (!s)
	{
		write(1, "(null)", 6);
		return (6);
	}
	a = 0;
	while (s[a] != '\0')
		a++;
	write(1, s, a);
	return (a);
}
